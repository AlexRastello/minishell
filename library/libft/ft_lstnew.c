#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*tmp;

	tmp = malloc(sizeof(t_list) * 1);
	if (!tmp)
		return (0);
	tmp->content = content;
	tmp->next = 0;
	return (tmp);
}
